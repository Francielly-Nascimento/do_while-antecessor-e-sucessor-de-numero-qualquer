#include <stdio.h>
#include <stdlib.h>
//2º problema: desenvolva um programa que leia uma sequência de números,
//podendo terminar com o número 0 ou 9. Para cada número lido (diferente de 0 ou 9),
//mostre seu sucessor caso o número seja par, ou seu antecessor se o número for ímpar.

int main()
{
    int num;
    do
    {
        printf("Digite um numero\n");
        scanf("%d",&num);
        if (num!=0 && num!=9)
        {
            if(num%2 == 0)
            {
                printf("O sucessor e = %d\n",num + 1);
            }
            else
            {
                printf("O antecessor e = %d\n",num -1);
            }

        }

    }
    while (num!=0 && num!=9);
    //comandos execultados apos a repetição
    return 0;
}
